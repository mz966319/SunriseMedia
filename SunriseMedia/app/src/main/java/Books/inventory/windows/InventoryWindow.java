    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        
        this.addTableModel.addRow(new String[]{jTextFieldBookID.getText(),jTextFieldBookTitle.getText(),jTextFieldQty.getText()});
        int qty=Integer.valueOf(jTextFieldQty.getText());
        if(this.invToAddList.containsKey(jTextFieldBookID.getText())){
            qty+=this.invToAddList.get(jTextFieldBookID.getText());
        }
        this.invToAddList.put(jTextFieldBookID.getText(), qty);

        jTextFieldBookID.setText("");
        jTextFieldBookTitle.setText("");
        jTextFieldQty.setText("");
        editModeAdd(false);

        
    }//GEN-LAST:event_jButtonAddActionPerformed

