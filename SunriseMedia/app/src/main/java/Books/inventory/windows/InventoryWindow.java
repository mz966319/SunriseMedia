    private void jTextFieldBookIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldBookIDKeyReleased
        // TODO add your handling code here:
        Book book = null;
        if(!this.jTextFieldBookID.getText().equals("")){
            book=SearchHelper.getBookByID(this.jTextFieldBookID.getText());
        }
            if(book!=null){
                this.jTextFieldBookTitle.setText(book.getTitle());
                editModeAdd(true);
            }
            else{
                this.jTextFieldBookTitle.setText("");
                editModeAdd(false);
            }
    }//GEN-LAST:event_jTextFieldBookIDKeyReleased

