    private void saveUpdateInventory(){
        ArrayList<Inventory> list= new ArrayList<>();
        String time = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss").format(LocalDateTime.now());
        String employeeID= USER.getId();//UserManger.getLocalUser();
        int rcvIndex = SearchHelper.getLastRcvIndex()+1;
        
        for (Map.Entry<String,Integer> entry : invToAddList.entrySet()){
            String id = UUID.randomUUID().toString();

            Inventory inventory = new Inventory(id,entry.getKey(),entry.getValue(),time,employeeID,rcvIndex);
            DatabaseHelper.addInventory(inventory);
            list.add(inventory);
            Book book=SearchHelper.getBookByID(entry.getKey());
            book.setQty(entry.getValue()+book.getQty());
            DatabaseHelper.updateBookQty(book);            
        }
        jTabbedPaneAll.add(String.valueOf(rcvIndex),new InventoryListPanel(list));
//        allTabPanel= new InventoryListPanel(SearchHelper.getInventories());
        allTabPanel.updateTableData(SearchHelper.getInventories());
        addTableModel.setRowCount(0);
        invToAddList.clear();
    }
    
    
